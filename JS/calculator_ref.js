let result = 0.0;
let operator = "+";
let inputNumber = "0";
let negative = true;

////////////////////
/* ===== setter, getter */
////////////////////

// setter
function setResult(value) {
    result = value;
}

function setOperator(value) {
    operator = value;
}

function setInputNumber(value) {
    inputnumber = value;
}

function setNegative(value) {
    negative = value;
}

// getter
function getResult() {
    return result;
}

function getOperator() {
    return operator;
}

function getInputNumber() {
    return inputnumber;
}

function getNegative() {
    return negative;
}
